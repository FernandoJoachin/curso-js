let resultado;

//PI
resultado = Math.PI;
console.log(resultado);

//Redondear
resultado = Math.round(4.8);
console.log(resultado);
resultado = Math.round(4.5);
console.log(resultado);
resultado = Math.round(4.4);
console.log(resultado);

//Redondear hacia arriba
resultado = Math.ceil(2.2);
console.log(resultado);

//Redondear hacia abajo
resultado = Math.floor(2.9);
console.log(resultado);

//Raiz cuadrada
resultado = Math.sqrt(169);
console.log(resultado);


//Absoluto
resultado = Math.abs(-100);
console.log(resultado);

//Potencia
resultado = Math.pow(2,4);
console.log(resultado);

//Minino
resultado = Math.min(3,1,10,200,-2);
console.log(resultado);

//Maximo
resultado = Math.max(3,1,10,200,-2);
console.log(resultado);

//Aleatorio
resultado = Math.random();
console.log(resultado);

//Aleatorio con rango
resultado = Math.floor(Math.random() * 100);
console.log(resultado);