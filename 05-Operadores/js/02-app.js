const numero = "10";
const numero2 = 10;
const numero3 = 30;

//Operador de igual
console.log(numero == numero3);
console.log(numero == numero2);

console.log(parseInt(numero) === numero2);
console.log(numero === numero2);

//Operador de diferente
console.log(numero != numero3);
console.log(numero != numero2);

console.log(parseInt(numero) !== numero2);
console.log(numero !== numero2);