const numero = 40;
const numero2 = 10;
let resultado;

//Suma
resultado = numero + numero2;
console.log(resultado);

//Resta
resultado = numero - numero2;
console.log(resultado);

//Multiplicacion
resultado = numero * numero2;
console.log(resultado);

//Division
resultado = numero / numero2;
console.log(resultado);

//Modulo
resultado = numero2 % numero;
console.log(resultado);