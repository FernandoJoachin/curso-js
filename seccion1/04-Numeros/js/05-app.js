//Incrementos
let puntaje = 10;
console.log(++puntaje);

puntaje = 10;
console.log(puntaje++);
console.log(puntaje);

puntaje = 10;
puntaje += 3;
console.log(puntaje);

//Decrementos
puntaje = 10;
console.log(--puntaje);

puntaje = 10;
console.log(puntaje--);
console.log(puntaje);

puntaje = 10;
puntaje -= 3;
console.log(puntaje);